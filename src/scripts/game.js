function generatePlaceholderPrice(price){
    let priceSalt = Math.random() * (Math.round(Math.random()) * 2 - 1);
    let placeHolder = price + (price * priceSalt) / 4;
    if (price < 10) {
        return (Math.ceil(placeHolder / 1) * 1)
    } else if (price < 100) {
        return (Math.ceil(placeHolder / 10) * 10)
    } else if (price < 1000) {
        return (Math.ceil(placeHolder / 100) * 100)
    } else {
        return (Math.ceil(placeHolder / 1000) * 1000)
    }
}


